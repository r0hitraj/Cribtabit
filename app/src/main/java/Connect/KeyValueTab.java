package Connect;

public class KeyValueTab {

    private String key;
    private String value;

    public KeyValueTab(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    // add two parameter constructor and getter-setter methods
}